# We cannot change the value of tuples
a = ()              #It is an example of empty tuple

a = (1,)           #Tuple with only one element needs a comma

a = (1, 7, 2)   #Tuple with more than one element

print(a.count(1)) # It will return the number of times 1 occurs in a.
print(a.index(1)) # It will return the index of the first occurrence of 1 in a.
