l1=[12,32,45,65,76]
print(l1)

l1.append(1234)  #add 1234 at the end of the list
print(l1)
l1.sort() #arrange the element of lists in increasing order
print(l1)
l1.reverse() #reverse the list
print(l1)
l1.insert(2, 567) #insert 567 on the index of 2
print(l1)
l1.sort()
print(l1)
l1.pop(2) #remove the element of the index of 2
print(l1)
l1.remove(1234) #remove 1234 from the list
print(l1)